OUT = r'C:\Users\motaz\OneDrive\Desktop\MyRX\tmp\movements_rename.xlsx'

wb = Workbook()
ws = wb.active
ws.title = 'Movements'

# Header
headers = ['Category', 'Current Name', 'New Name']
ws.append(headers)

header_font   = Font(name='Arial', size=11, bold=True, color='FFFFFF')
header_fill   = PatternFill('solid', start_color='1F2937')  # slate-800
center        = Alignment(horizontal='center', vertical='center')
left          = Alignment(horizontal='left', vertical='center')
thin          = Side(style='thin', color='D1D5DB')          # gray-300
border        = Border(left=thin, right=thin, top=thin, bottom=thin)

for col_idx in range(1, 4):
    cell = ws.cell(row=1, column=col_idx)
    cell.font = header_font
    cell.fill = header_fill
    cell.alignment = center
    cell.border = border

# Data rows
strength_fill = PatternFill('solid', start_color='EFF6FF')  # blue-50
cardio_fill   = PatternFill('solid', start_color='FFFBEB')  # amber-50
new_name_fill = PatternFill('solid', start_color='FEFCE8')  # yellow-50 (highlight col C as the editable one)

for r_idx, (category, name) in enumerate(ROWS, start=2):
    ws.cell(row=r_idx, column=1, value=category)
    ws.cell(row=r_idx, column=2, value=name)
    ws.cell(row=r_idx, column=3, value='')

    cat_fill = strength_fill if category == 'strength' else cardio_fill
    for col_idx in range(1, 4):
        c = ws.cell(row=r_idx, column=col_idx)
        c.font = Font(name='Arial', size=10)
        c.alignment = left
        c.border = border
        # Category column gets the cat colour; new-name col gets yellow tint;
        # current-name uses the cat colour to keep rows visually grouped.
        if col_idx == 3:
            c.fill = new_name_fill
        else:
            c.fill = cat_fill

# Column widths — measured to fit content comfortably
ws.column_dimensions['A'].width = 12
ws.column_dimensions['B'].width = 42
ws.column_dimensions['C'].width = 42

# Freeze header row + first column for easy scrolling
ws.freeze_panes = 'B2'

# Bold the category column
for r_idx in range(2, len(ROWS) + 2):
    ws.cell(row=r_idx, column=1).font = Font(name='Arial', size=10, bold=True)

wb.save(OUT)
print(f'Wrote {OUT}')
print(f'Total rows: {len(ROWS)} ({sum(1 for r in ROWS if r[0]=="cardio")} cardio + {sum(1 for r in ROWS if r[0]=="strength")} strength)')
